
package jdbc;

public class Clavo {
    
    private int ID_Clavo;
    private float Tamaño;
    private String Material;
    private float PrecioXpieza;

    public int getID_Clavo() {
        return ID_Clavo;
    }

    public void setID_Clavo(int ID_Clavo) {
        this.ID_Clavo = ID_Clavo;
    }

    public float getTamaño() {
        return Tamaño;
    }

    public void setTamaño(float Tamaño) {
        this.Tamaño = Tamaño;
    }

    public String getMaterial() {
        return Material;
    }

    public void setMaterial(String Material) {
        this.Material = Material;
    }

    public float getPrecioXpieza() {
        return PrecioXpieza;
    }

    public void setPrecioXpieza(float PrecioXpieza) {
        this.PrecioXpieza = PrecioXpieza;
    }

    
}
